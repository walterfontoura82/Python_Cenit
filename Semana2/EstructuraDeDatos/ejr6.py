# 6-Crear un set/conjunto con los números impares del 1 al 10 y mostrar el número
# de elementos en el conjunto.


conjunto = set(range(1, 22, 4))  # Crea el conjunto con los números impares del 1 al 10
numero_elementos = len(conjunto)  # Obtiene el número de elementos en el conjunto

print("Conjunto:", conjunto)
print("Número de elementos:", numero_elementos)



